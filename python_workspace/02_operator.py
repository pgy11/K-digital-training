# 산술 연산자 : +, -, *, /, %
x = 10
y = 20
print('x + y =', x + y)
print('x - y =', x - y)
print('x * y =', x * y)
print('x / y =', x / y)
print('x % y =', x % y)
print('2 ** 3 = ', 2 ** 3)
